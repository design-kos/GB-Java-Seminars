package Seminars.Seminar8;

public class Student {

    private String name;
    private float avgScore;

    public Student(String name, float avgScore) {
        this.name = name;
        this.avgScore = avgScore;
    }

    public String getName() {
        return name;
    }

    public float getAvgScore() {
        return avgScore;
    }

}
